export function normalizeLanguageCode(value) {
  const code = String(value || 'en').toLowerCase();
  if (code === 'tl') return 'fil';
  if (code.startsWith('fil')) return 'fil';
  if (code.startsWith('es')) return 'es';
  return 'en';
}

export function translate(language, key, fallback) {
  const normalized = normalizeLanguageCode(language);
  const table = translations[normalized] || translations.en;
  return table[key] || translations.en[key] || fallback || key;
}

export function translateLiteral(language, text) {
  const normalized = normalizeLanguageCode(language);
  return literalTranslations[normalized]?.[text] || text;
}
